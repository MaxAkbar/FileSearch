using System.Globalization;
using System.IO.Compression;
using System.Text;
using System.Text.Json;
using DocumentFormat.OpenXml;
using DocumentFormat.OpenXml.Packaging;
using DocumentFormat.OpenXml.Spreadsheet;
using DocumentFormat.OpenXml.Wordprocessing;
using Text = DocumentFormat.OpenXml.Wordprocessing.Text;
using WordRun = DocumentFormat.OpenXml.Wordprocessing.Run;

namespace FileSearch.Benchmarks;

internal sealed class BenchmarkCorpusGenerator
{
    private const int Seed = 0x5EED2026;

    public CorpusManifest EnsureCorpus(BenchmarkPaths paths, bool force)
    {
        if (File.Exists(paths.ManifestPath) && !force)
        {
            var existing = JsonSerializer.Deserialize(
                File.ReadAllText(paths.ManifestPath),
                BenchmarkJsonContext.Default.CorpusManifest);
            if (existing is not null &&
                string.Equals(existing.Profile, paths.Profile.Name, StringComparison.Ordinal))
            {
                return existing;
            }
        }

        ResetCorpus(paths);
        Directory.CreateDirectory(paths.ContentRoot);
        Directory.CreateDirectory(paths.MetadataRoot);
        Directory.CreateDirectory(Path.GetDirectoryName(paths.DatabasePath)!);
        Directory.CreateDirectory(paths.ReportsDirectory);
        File.WriteAllText(paths.MarkerPath, "Generated by FileSearch.Benchmarks.");

        var physicalFiles = 0;
        var relevant = new Dictionary<string, List<string>>(StringComparer.Ordinal);
        var expectedTop = new Dictionary<string, string>(StringComparer.Ordinal);

        physicalFiles += GenerateSmallTextAndSourceFiles(paths, relevant, expectedTop);
        physicalFiles += GenerateOfficeDocuments(paths, relevant, expectedTop);
        physicalFiles += GeneratePdfDocuments(paths, relevant, expectedTop);
        physicalFiles += GenerateLargeLogs(paths, relevant, expectedTop);
        physicalFiles += GenerateArchives(paths, relevant, expectedTop);
        physicalFiles += GenerateUnicodeAndLongPaths(paths, relevant, expectedTop);

        var metadataTopPath = Path.Combine(
            paths.MetadataRoot,
            "virtual",
            "bucket_0042",
            "metadata_target_000042.cs");
        relevant["metadata_target_000042"] = [metadataTopPath];
        expectedTop["metadata_target_000042"] = metadataTopPath;

        var queries = new[]
        {
            Query("metadata-exact", "metadata_target_000042", "metadata", relevant, expectedTop, includeExpectedTop: true),
            Query("source-content", "alpha_customer", "content", relevant, expectedTop),
            Query("office-content", "quarterly_revenue_model", "content", relevant, expectedTop),
            Query("pdf-content", "pdf_contract_marker", "content", relevant, expectedTop),
            Query("log-content", "critical_latency_event", "content", relevant, expectedTop),
            Query("archive-content", "archive_policy_marker", "content", relevant, expectedTop),
            Query("unicode-content", "unicode_cafe_marker", "content", relevant, expectedTop),
        };

        var manifest = new CorpusManifest(
            paths.Profile.Name,
            Seed,
            DateTime.UtcNow,
            paths.ContentRoot,
            paths.MetadataRoot,
            paths.Profile.MetadataOnlyEntryCount,
            physicalFiles,
            queries,
            BuildExternalRootProbes());

        File.WriteAllText(
            paths.ManifestPath,
            JsonSerializer.Serialize(manifest, BenchmarkJsonContext.Default.CorpusManifest));
        return manifest;
    }

    private static BenchmarkQuery Query(
        string name,
        string query,
        string rootKind,
        Dictionary<string, List<string>> relevant,
        Dictionary<string, string> expectedTop,
        bool includeExpectedTop = false) =>
        new(
            name,
            query,
            rootKind,
            relevant.TryGetValue(query, out var paths) ? paths.OrderBy(path => path, StringComparer.OrdinalIgnoreCase).ToArray() : [],
            includeExpectedTop && expectedTop.TryGetValue(query, out var top) ? top : null);

    private static void ResetCorpus(BenchmarkPaths paths)
    {
        if (!Directory.Exists(paths.CorpusDirectory))
            return;

        if (!File.Exists(paths.MarkerPath))
            throw new InvalidOperationException($"Refusing to overwrite unmarked benchmark directory: {paths.CorpusDirectory}");

        Directory.Delete(paths.CorpusDirectory, recursive: true);
    }

    private static int GenerateSmallTextAndSourceFiles(
        BenchmarkPaths paths,
        Dictionary<string, List<string>> relevant,
        Dictionary<string, string> expectedTop)
    {
        var count = 0;
        var targetPaths = new List<string>();
        for (var i = 0; i < paths.Profile.SmallTextFileCount; i++)
        {
            var extension = i % 5 == 0 ? ".md" : i % 3 == 0 ? ".json" : ".cs";
            var folder = Path.Combine(paths.ContentRoot, "src", $"module_{i % 128:D3}");
            Directory.CreateDirectory(folder);
            var marker = i % 97 == 0 ? "alpha_customer" : $"ordinary_token_{i % 31:D2}";
            var fileName = marker == "alpha_customer"
                ? $"alpha_customer_{i:D6}{extension}"
                : $"source_{i:D6}{extension}";
            var path = Path.Combine(folder, fileName);
            File.WriteAllText(
                path,
                $$"""
                // deterministic benchmark file {{i.ToString(CultureInfo.InvariantCulture)}}
                namespace Benchmark.Module{{(i % 128).ToString(CultureInfo.InvariantCulture)}};
                public static class Fixture{{i.ToString(CultureInfo.InvariantCulture)}} {
                    public const string Token = "{{marker}}";
                    public const string Body = "stable content search fixture {{i.ToString(CultureInfo.InvariantCulture)}}";
                }
                """);

            if (marker == "alpha_customer")
                targetPaths.Add(path);

            count++;
        }

        relevant["alpha_customer"] = targetPaths;
        expectedTop["alpha_customer"] = targetPaths.OrderBy(path => path, StringComparer.OrdinalIgnoreCase).First();
        return count;
    }

    private static int GenerateOfficeDocuments(
        BenchmarkPaths paths,
        Dictionary<string, List<string>> relevant,
        Dictionary<string, string> expectedTop)
    {
        var folder = Path.Combine(paths.ContentRoot, "documents", "office");
        Directory.CreateDirectory(folder);
        var targets = new List<string>();
        for (var i = 0; i < paths.Profile.OfficeDocumentCount; i++)
        {
            var path = Path.Combine(
                folder,
                i % 2 == 0
                    ? $"quarterly_revenue_model_{i:D4}.docx"
                    : $"quarterly_revenue_model_{i:D4}.xlsx");
            if (i % 2 == 0)
                CreateDocx(path, $"quarterly_revenue_model office benchmark document {i}");
            else
                CreateXlsx(path, $"quarterly_revenue_model office benchmark workbook {i}");

            targets.Add(path);
        }

        relevant["quarterly_revenue_model"] = targets;
        expectedTop["quarterly_revenue_model"] = targets.OrderBy(path => path, StringComparer.OrdinalIgnoreCase).First();
        return targets.Count;
    }

    private static int GeneratePdfDocuments(
        BenchmarkPaths paths,
        Dictionary<string, List<string>> relevant,
        Dictionary<string, string> expectedTop)
    {
        var folder = Path.Combine(paths.ContentRoot, "documents", "pdf");
        Directory.CreateDirectory(folder);
        var targets = new List<string>();
        for (var i = 0; i < paths.Profile.PdfDocumentCount; i++)
        {
            var path = Path.Combine(folder, $"pdf_contract_marker_{i:D4}.pdf");
            CreateSimplePdf(path, $"pdf_contract_marker deterministic pdf fixture {i}");
            targets.Add(path);
        }

        relevant["pdf_contract_marker"] = targets;
        expectedTop["pdf_contract_marker"] = targets.OrderBy(path => path, StringComparer.OrdinalIgnoreCase).First();
        return targets.Count;
    }

    private static int GenerateLargeLogs(
        BenchmarkPaths paths,
        Dictionary<string, List<string>> relevant,
        Dictionary<string, string> expectedTop)
    {
        var folder = Path.Combine(paths.ContentRoot, "logs");
        Directory.CreateDirectory(folder);
        var targets = new List<string>();
        for (var i = 0; i < paths.Profile.LargeLogFileCount; i++)
        {
            var path = Path.Combine(folder, $"service_{i:D3}.log");
            using var writer = new StreamWriter(path, append: false, Encoding.UTF8);
            for (var line = 0; line < paths.Profile.LargeLogLinesPerFile; line++)
            {
                var marker = line % 997 == 0 ? "critical_latency_event" : "info_event";
                writer.WriteLine(string.Format(
                    CultureInfo.InvariantCulture,
                    "2026-01-01T00:{0:D2}:{1:D2}Z {2} service={3:D3} sequence={4:D8}",
                    line % 60,
                    line % 60,
                    marker,
                    i,
                    line));
            }

            targets.Add(path);
        }

        relevant["critical_latency_event"] = targets;
        expectedTop["critical_latency_event"] = targets.OrderBy(path => path, StringComparer.OrdinalIgnoreCase).First();
        return targets.Count;
    }

    private static int GenerateArchives(
        BenchmarkPaths paths,
        Dictionary<string, List<string>> relevant,
        Dictionary<string, string> expectedTop)
    {
        var folder = Path.Combine(paths.ContentRoot, "archives");
        Directory.CreateDirectory(folder);
        var targets = new List<string>();
        for (var i = 0; i < paths.Profile.ArchiveCount; i++)
        {
            var path = Path.Combine(folder, $"archive_policy_marker_{i:D4}.zip");
            using var archive = ZipFile.Open(path, ZipArchiveMode.Create);
            for (var entryIndex = 0; entryIndex < paths.Profile.ArchiveEntriesPerFile; entryIndex++)
            {
                var entry = archive.CreateEntry($"entry_{entryIndex:D4}.txt", CompressionLevel.Fastest);
                using var stream = entry.Open();
                using var writer = new StreamWriter(stream, Encoding.UTF8);
                writer.WriteLine($"archive_policy_marker archive={i} entry={entryIndex}");
            }

            targets.Add(path);
        }

        relevant["archive_policy_marker"] = targets;
        expectedTop["archive_policy_marker"] = targets.OrderBy(path => path, StringComparer.OrdinalIgnoreCase).First();
        return targets.Count;
    }

    private static int GenerateUnicodeAndLongPaths(
        BenchmarkPaths paths,
        Dictionary<string, List<string>> relevant,
        Dictionary<string, string> expectedTop)
    {
        var targets = new List<string>();
        for (var i = 0; i < paths.Profile.UnicodeAndLongPathFileCount; i++)
        {
            var folder = Path.Combine(
                paths.ContentRoot,
                "unicode",
                "cafe-naive-resume",
                $"segment-{i % 17:D2}-long-path-fixture");
            Directory.CreateDirectory(folder);
            var path = Path.Combine(folder, $"unicode_cafe_marker_é_文件_{i:D5}.txt");
            File.WriteAllText(path, $"unicode_cafe_marker café naïve résumé 文件 fixture {i}", Encoding.UTF8);
            targets.Add(path);
        }

        relevant["unicode_cafe_marker"] = targets;
        expectedTop["unicode_cafe_marker"] = targets.OrderBy(path => path, StringComparer.OrdinalIgnoreCase).First();
        return targets.Count;
    }

    private static IReadOnlyList<ExternalRootProbe> BuildExternalRootProbes()
    {
        return
        [
            Probe("network", Environment.GetEnvironmentVariable("FILESEARCH_BENCHMARK_NETWORK_ROOT"), "scheduled snapshot scan; watcher best effort"),
            Probe("removable", Environment.GetEnvironmentVariable("FILESEARCH_BENCHMARK_REMOVABLE_ROOT"), "offline cache plus reconnect scan"),
            Probe("cloud", ResolveCloudRoot(), "snapshot scan plus watcher; hydration delays tolerated"),
        ];
    }

    private static ExternalRootProbe Probe(string kind, string? path, string strategy) =>
        new(kind, path, !string.IsNullOrWhiteSpace(path) && Directory.Exists(path), strategy);

    private static string? ResolveCloudRoot() =>
        Environment.GetEnvironmentVariable("FILESEARCH_BENCHMARK_CLOUD_ROOT") ??
        Environment.GetEnvironmentVariable("OneDriveCommercial") ??
        Environment.GetEnvironmentVariable("OneDriveConsumer") ??
        Environment.GetEnvironmentVariable("OneDrive");

    private static void CreateDocx(string path, string text)
    {
        using var document = WordprocessingDocument.Create(path, WordprocessingDocumentType.Document);
        var mainPart = document.AddMainDocumentPart();
        mainPart.Document = new Document(
            new Body(
                new Paragraph(
                    new WordRun(
                        new Text(text)))));
        mainPart.Document.Save();
    }

    private static void CreateXlsx(string path, string text)
    {
        using var document = SpreadsheetDocument.Create(path, SpreadsheetDocumentType.Workbook);
        var workbookPart = document.AddWorkbookPart();
        workbookPart.Workbook = new Workbook();
        var worksheetPart = workbookPart.AddNewPart<WorksheetPart>();
        worksheetPart.Worksheet = new Worksheet(
            new SheetData(
                new Row(
                    new Cell
                    {
                        DataType = CellValues.String,
                        CellValue = new CellValue(text),
                    })));
        var sheets = workbookPart.Workbook.AppendChild(new Sheets());
        sheets.Append(new Sheet
        {
            Id = workbookPart.GetIdOfPart(worksheetPart),
            SheetId = 1,
            Name = "Benchmark",
        });
        workbookPart.Workbook.Save();
    }

    private static void CreateSimplePdf(string path, string text)
    {
        static string Escape(string value) =>
            value.Replace(@"\", @"\\", StringComparison.Ordinal)
                .Replace("(", @"\(", StringComparison.Ordinal)
                .Replace(")", @"\)", StringComparison.Ordinal);

        var escapedText = Escape(text);
        var pdfTextStream = $"BT /F1 12 Tf 72 720 Td ({escapedText}) Tj ET\n";
        var objects = new[]
        {
            "<< /Type /Catalog /Pages 2 0 R >>",
            "<< /Type /Pages /Kids [3 0 R] /Count 1 >>",
            "<< /Type /Page /Parent 2 0 R /MediaBox [0 0 612 792] /Resources << /Font << /F1 4 0 R >> >> /Contents 5 0 R >>",
            "<< /Type /Font /Subtype /Type1 /BaseFont /Helvetica >>",
            $"<< /Length {Encoding.ASCII.GetByteCount(pdfTextStream)} >>\nstream\n{pdfTextStream}endstream",
        };

        using var stream = File.Create(path);
        using var writer = new StreamWriter(stream, Encoding.ASCII, leaveOpen: true);
        writer.WriteLine("%PDF-1.4");
        var offsets = new List<long> { 0 };
        for (var i = 0; i < objects.Length; i++)
        {
            writer.Flush();
            offsets.Add(stream.Position);
            writer.WriteLine(string.Format(CultureInfo.InvariantCulture, "{0} 0 obj", i + 1));
            writer.WriteLine(objects[i]);
            writer.WriteLine("endobj");
        }

        writer.Flush();
        var xref = stream.Position;
        writer.WriteLine("xref");
        writer.WriteLine(string.Format(CultureInfo.InvariantCulture, "0 {0}", objects.Length + 1));
        writer.WriteLine("0000000000 65535 f ");
        foreach (var offset in offsets.Skip(1))
            writer.WriteLine(string.Format(CultureInfo.InvariantCulture, "{0:D10} 00000 n ", offset));

        writer.WriteLine("trailer");
        writer.WriteLine(string.Format(CultureInfo.InvariantCulture, "<< /Size {0} /Root 1 0 R >>", objects.Length + 1));
        writer.WriteLine("startxref");
        writer.WriteLine(string.Format(CultureInfo.InvariantCulture, "{0}", xref));
        writer.WriteLine("%%EOF");
    }
}
